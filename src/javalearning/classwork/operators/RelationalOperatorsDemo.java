public class RelationalOperatorsDemo {
    // Relational operator always give true and false //
    public static void main(String []args ){
        int a=10;
        int b=15;
        System.out.println("a==b:" + (a==b));
        float c =10f;
        float d =15f;
        System.out.println("c==d:" + (c==d));
        System.out.println("c!=d:"+ (c!=d) );
        System.out.println("c>d:"+ (c>d) );
        System.out.println("c<=d:"+(c<=d));
        System.out.println("c>=d:"+(c>=d));
        
       
        
    }
    
}
